/*
    Write Bangladesh in 10 Times
*/

#include <iostream>
using namespace std;

int main()
{
    int n = 1;
    do
    {
        cout << "Bangladesh!!" << endl;
        n++;
    } while (n <= 10);
    return 0;
}

