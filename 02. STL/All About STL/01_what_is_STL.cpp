/*
 * File: 01_Intro_to_STL.cpp
 * ------------------------
 * The Standard Template Library (STL) is a powerful set of C++ template classes 
 * that provide ready-to-use data structures and algorithms.
 *
 * It consists of four main components:
 * 1. Containers: Used to manage collections of objects (e.g., vector, list, map).
 * 2. Algorithms: Act on containers (e.g., sort, search, transform).
 * 3. Iterators: The bridge between algorithms and containers.
 * 4. Functors: Objects that can be treated as functions.
 *
 * Why use STL?
 * - Efficiency: Highly optimized by compiler experts.
 * - Safety: Reduces the risk of manual memory management errors.
 * - Reusability: Generic code that works with any data type.
 */